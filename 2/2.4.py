import sympy as sp

# Параметры параболического цилиндра
a = float(input("Введите параметр a параболического цилиндра: "))
b = float(input("Введите параметр b параболического цилиндра: "))

# Координаты точки
x1 = float(input("Введите координату x точки: "))
y1 = float(input("Введите координату y точки: "))
z1 = float(input("Введите координату z точки: "))

# Уравнение плоскости
A = float(input("Введите коэффициент A плоскости: "))
B = float(input("Введите коэффициент B плоскости: "))
C = float(input("Введите коэффициент C плоскости: "))
D = float(input("Введите коэффициент D плоскости: "))

# Параметры прямой
x0 = float(input("Введите координату x точки на прямой: "))
y0 = float(input("Введите координату y точки на прямой: "))
z0 = float(input("Введите координату z точки на прямой: "))
d1 = float(input("Введите координату x направляющего вектора прямой: "))
d2 = float(input("Введите координату y направляющего вектора прямой: "))
d3 = float(input("Введите координату z направляющего вектора прямой: "))

# Проверка принадлежности точки параболическому цилиндру
def is_point_on_parabolic_cylinder(a, b, x1, y1, z1):
    return z1 == a * x1**2 + b * y1**2

if is_point_on_parabolic_cylinder(a, b, x1, y1, z1):
    print("Точка принадлежит параболическому цилиндру.")
else:
    print("Точка не принадлежит параболическому цилиндру.")

# Проверка пересечения плоскости с параболическим цилиндром
x, y = sp.symbols('x y')
z = a * x**2 + b * y**2
plane_eq = A * x + B * y + C * z + D
sol = sp.solve(plane_eq, z)

if sol:
    print("Плоскость пересекает параболический цилиндр.")
else:
    print("Плоскость не пересекает параболический цилиндр.")

# Проверка пересечения прямой с параболическим цилиндром
t = sp.symbols('t')
x_t = x0 + d1 * t
y_t = y0 + d2 * t
z_t = z0 + d3 * t
cylinder_eq = z_t - (a * x_t**2 + b * y_t**2)
sol_t = sp.solve(cylinder_eq, t)

if sol_t:
    print("Прямая пересекает параболический цилиндр.")
else:
    print("Прямая не пересекает параболический цилиндр.")